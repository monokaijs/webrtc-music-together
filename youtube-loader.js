function onYouTubeIframeAPIReady() {
  player = new window.YT.Player('player', {
    height: 0,
    width: 0,
    videoId: 'M7lc1UVf-VE',
    playerVars: {
      'playsinline': 1
    },
    events: {
      'onReady': onPlayerReady,
      'onStateChange': onPlayerStateChange
    }
  });
}

function onPlayerReady(event) {
  event.target.playVideo();
  playerReady = true;
}

